public class Student {
    int id;
    String name, email, phone, batch, section;

    Student(int id, String name, String email, String phone, String batch, String section) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.batch = batch;
        this.section = section;
    }
}
